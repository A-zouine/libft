#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int i, char *str))
{
	int	i;

	i = 0;
	if (!s || !f)
		return ;
	while (s[i])
	{
		f(i, &s[i]);
		i++;
	}
}
